// Comparison Operators

// Equal to
if (5 == '5'){
    console.log(true);
};

// Equal Value and Type

if (5 === 5){
    console.log(true);
};

// Not Equal to

if (5 != '4'){
    console.log(true);
};

// Not Equal Value and Type
if (5 != 4){
    console.log(true);
};

// Greater Than
if (5 > 4){
    console.log(true);
};

// Less Than
if (5 < 6){
    console.log(true);
};

// Greater Than or Equal to
if (5 >= 5){
    console.log(true);
};

// Less Than or Equal to
if (5 <= 5){
    console.log(true);
};

// And
2 && 3;

// Or
2 || 3;
